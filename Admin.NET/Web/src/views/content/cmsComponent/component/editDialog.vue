<script lang="ts" name="cmsComponent" setup>
import { ref, reactive, onMounted } from "vue";
import { ElMessage } from "element-plus";
import type { FormRules } from "element-plus";
import { formatDate } from '/@/utils/formatTime';
import { useCmsComponentApi } from '/@/api/content/cmsComponent';
//父级传递来的函数，用于回调
const emit = defineEmits(["reloadTable"]);
const cmsComponentApi = useCmsComponentApi();
const ruleFormRef = ref();

const state = reactive({
	title: '',
	loading: false,
	showDialog: false,
	ruleForm: {} as any,
	stores: {},
	dropdownData: {} as any,
});

// 自行添加其他规则
const rules = ref<FormRules>({
  pageId: [{required: true, message: '请选择PageId！', trigger: 'blur',},],
  componentTypeId: [{required: true, message: '请选择ComponentTypeId！', trigger: 'blur',},],
  pid: [{required: true, message: '请选择Pid！', trigger: 'blur',},],
  props: [{required: true, message: '请选择Props！', trigger: 'blur',},],
  sortOrder: [{required: true, message: '请选择SortOrder！', trigger: 'blur',},],
  isVisible: [{required: true, message: '请选择IsVisible！', trigger: 'blur',},],
});

// 页面加载时
onMounted(async () => {
});

// 打开弹窗
const openDialog = async (row: any, title: string) => {
	state.title = title;
	row = row ?? { styles: "NULL::character varying",createUserName: "NULL::character varying",updateUserName: "NULL::character varying", };
	state.ruleForm = row.id ? await cmsComponentApi.detail(row.id).then(res => res.data.result) : JSON.parse(JSON.stringify(row));
	state.showDialog = true;
};

// 关闭弹窗
const closeDialog = () => {
	emit("reloadTable");
	state.showDialog = false;
};

// 提交
const submit = async () => {
	ruleFormRef.value.validate(async (isValid: boolean, fields?: any) => {
		if (isValid) {
			let values = state.ruleForm;
			await cmsComponentApi[state.ruleForm.id ? 'update' : 'add'](values);
			closeDialog();
		} else {
			ElMessage({
				message: `表单有${Object.keys(fields).length}处验证失败，请修改后再提交`,
				type: "error",
			});
		}
	});
};

//将属性或者函数暴露给父组件
defineExpose({ openDialog });
</script>
<template>
	<div class="cmsComponent-container">
		<el-dialog v-model="state.showDialog" :width="800" draggable :close-on-click-modal="false">
			<template #header>
				<div style="color: #fff">
					<span>{{ state.title }}</span>
				</div>
			</template>
			<el-form :model="state.ruleForm" ref="ruleFormRef" label-width="auto" :rules="rules">
				<el-row :gutter="35">
					<el-form-item v-show="false">
						<el-input v-model="state.ruleForm.id" />
					</el-form-item>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="PageId" prop="pageId">
							<el-input v-model="state.ruleForm.pageId" placeholder="请输入PageId" show-word-limit clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="ComponentTypeId" prop="componentTypeId">
							<el-input v-model="state.ruleForm.componentTypeId" placeholder="请输入ComponentTypeId" show-word-limit clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="Pid" prop="pid">
							<el-input v-model="state.ruleForm.pid" placeholder="请输入Pid" show-word-limit clearable />
						</el-form-item>
					</el-col>
					<el-col :xs="24" class="mb20" >
						<el-form-item label="Props" prop="props">
							<el-input v-model="state.ruleForm.props" placeholder="请输入Props" maxlength="1000" type="textarea" show-word-limit clearable />
						</el-form-item>
					</el-col>
					<el-col :xs="24" class="mb20" >
						<el-form-item label="Styles" prop="styles">
							<el-input v-model="state.ruleForm.styles" placeholder="请输入Styles" maxlength="500" type="textarea" show-word-limit clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="SortOrder" prop="sortOrder">
							<el-input-number v-model="state.ruleForm.sortOrder" placeholder="请输入SortOrder" clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="IsVisible" prop="isVisible">
							<el-switch v-model="state.ruleForm.isVisible" />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="DeleteTime" prop="deleteTime">
							<el-date-picker v-model="state.ruleForm.deleteTime" type="date" placeholder="DeleteTime" />
						</el-form-item>
					</el-col>
				</el-row>
			</el-form>
			<template #footer>
				<span class="dialog-footer">
					<el-button @click="() => state.showDialog = false">取 消</el-button>
					<el-button @click="submit" type="primary" v-reclick="1000">确 定</el-button>
				</span>
			</template>
		</el-dialog>
	</div>
</template>
<style lang="scss" scoped>
:deep(.el-select), :deep(.el-input-number) {
  width: 100%;
}
</style>